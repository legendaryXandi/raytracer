package basics;

public class Fallof {
    private int alpha1;
    private int alpha2;

    public Fallof(int alpha1, int alpha2) {
        this.alpha1 = alpha1;
        this.alpha2 = alpha2;
    }

    public Fallof() {
    }

    public int getAlpha1() {
        return alpha1;
    }

    public void setAlpha1(int alpha1) {
        this.alpha1 = alpha1;
    }

    public int getAlpha2() {
        return alpha2;
    }

    public void setAlpha2(int alpha2) {
        this.alpha2 = alpha2;
    }
}
